    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $input = $request->all();

        (isset($input['photo_background'])) ? $namaPhoto = str_random().'.'.$input['photo_background']->getClientOriginalExtension() : $namaPhoto = null;
        
        Slider::create([
            'title' => $input['title'],
            'description' => $input['description'],
            'photo_background' => $namaPhoto
        ]);

        (isset($input['photo_background'])) ? $input['photo_background']->move(public_path('slider'), $namaPhoto) : null;

        return redirect('/admin/slider');
    }

